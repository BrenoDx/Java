    private void BtFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtFinalizarActionPerformed
        // TODO add your handling code here: 
        if (paciente != null) {
            String receita = TxaReceita.getText();
        if (!receita.isEmpty() && paciente.getIsStatus()) {
            paciente.setReceita(receita);
            telaInicial.atualizarTable();
            JOptionPane.showMessageDialog(this, "Consulta finalizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            dispose();
         } else {
            JOptionPane.showMessageDialog(this, "Por favor, insira a receita ou observação e sinaliza caixa de consulta realizada antes de finalizar.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }
           
    }//GEN-LAST:event_BtFinalizarActionPerformed

