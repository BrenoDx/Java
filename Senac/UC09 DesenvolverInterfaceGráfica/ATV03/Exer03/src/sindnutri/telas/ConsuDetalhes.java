    private void exibir(){
    if (paciente != null && paciente.getIsStatus()) {
            CbConsulta.setSelected(true);
            CbConsulta.setEnabled(false); // Desabilita checkbox para consulta realizada
            BtFinalizar.setVisible(false); // Esconde o botão de finalizar se a consulta já foi realizada
            TxaReceita.setText(paciente.getReceita()); // Exibe a receita
        } else {
            CbConsulta.setEnabled(true); // Habilita checkbox se a consulta não foi realizada
            BtFinalizar.setVisible(true); // Exibe o botão de finalizar
        }
    }
    
